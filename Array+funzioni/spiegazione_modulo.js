// % è l'operatore MODULO
// Serve per trovare il RESTO di una divisione

console.log(10 % 2); // 0
// 10 diviso 2 = 5
// resto = 0

console.log(7 % 2); // 1
// 7 diviso 2 = 3
// resto = 1

console.log(15 % 4); // 3
// 15 diviso 4 = 3
// resto = 3



// COME SI USA PER CAPIRE SE UN NUMERO È PARI

let numero = 8;

if (numero % 2 === 0) {
  console.log("Numero pari");
} else {
  console.log("Numero dispari");
}



// ESEMPI

console.log(4 % 2); // 0 → pari
console.log(5 % 2); // 1 → dispari
console.log(12 % 2); // 0 → pari
console.log(9 % 2); // 1 → dispari